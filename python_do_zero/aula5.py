# Tipo de dado bool (boolean)

# Ao questionar algo em algum programa, só existem duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar". Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro.

print(10 == 10) # Sim => True (verdadeiro)
print(10 == 11) # Não => False (falso)

# Utilizando a função type podemos verificar somente o tipo no retorno

print(type(10 == 10))
print(type(10 == 10))


