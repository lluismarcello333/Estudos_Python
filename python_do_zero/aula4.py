# Tipos Primitivos

# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número positivo ou negativo. int sem sinal é considerado positivo
print(11) # int
print(-11) # int
print(0) # Número literal inteiro

# float -> Número vom ponto flutuante
# O tipo float representa qualquer número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11) # Na programação sempre utilizamos o '.' para separar os números com vírgula. A vírgula é utilizada para separar argumentos.
print(0.0, -1.5) 

# A função type mostra o tipo que o Python inferiu o valor.
print(type('Luiz'))
print(type(1))
print(type(-1))
print(type(0))
print(type(1.1))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))

