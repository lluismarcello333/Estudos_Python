'''
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''

# Ele já sabe que no exemplo abaixo o tipo é int e consegue exibir o script
print(1234)

# Aspas Simples
print('Posso escrever um texto dentro de aspas simples. E será entendido que estamos usando strings')

# Aspas Duplas
print("Posso escrever um texto dentro de aspas duplas. E será entendido que estamos usando strings")

# Escape
print('Posso escrever um texto e utilizar aspas dentro do print usando o \"Escape\". E será entendido que estamos usando strings')

# r
print(r"Posso escrever um texto e utilizar o \"r\". E será entendido que estamos usando strings")


#Dica:
#      Podemos utilizar aspas duplas ou aspas simples uma dentro da outra. Dessa forma, será possível "printar" as aspas.
print(1, 'Luiz "Marcelo"')
print(2, "Luiz 'Marcelo'")


