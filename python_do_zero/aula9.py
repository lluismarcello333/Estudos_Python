adicao = 10 + 10 
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.6
print('Divisão', divisao) # Sempre o retorno será float

divisao_inteira = 10 // 2.6
print('Divisão inteira', divisao_inteira) # Nem sempre retornará um número inteiro. Dependo do Divisor. Esse tipo de divisão inteira será limitado na parte decimal

exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2 # Retorna o resto da divisão
print('Módulo', modulo)

# Podemos realizar a seguinte revisão:
print(10 % 8 == 0) # Será que o número 10 é divisível por 8?
print(16 % 8 == 0) # O némro 16 é divisível por 8?
print(10 % 2 == 0) # O número dez pe divisível por 2. Se for, ele é PAR.
print(15 % 2 == 0) # Então podemos confirmar que o número 15 é ÍMPAR
